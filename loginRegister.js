function registration(){
    var log = document.getElementById("login");
    log.style.display = "none";

    var reg = document.getElementById("register");
    reg.style.display = "block";
}

function login(){
    var reg = document.getElementById("register");
    reg.style.display = "none";
    
    var log = document.getElementById("login");
    log.style.display = "block"



}